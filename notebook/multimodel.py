class GenomicBERT(nn.Module):

    def __init__(self, n_genes: int, config: dict):
        super().__init__()
        H = config["genomic_hidden_size"]

        self.gene_proj = nn.Linear(1, H)
        self.pos_emb   = nn.Embedding(n_genes + 1, H)
        self.norm      = nn.LayerNorm(H)
        self.drop      = nn.Dropout(config["genomic_dropout"])

        bert_cfg = BertConfig(
            hidden_size              = H,
            num_attention_heads      = config["genomic_attn_heads"],
            num_hidden_layers        = config["genomic_layers"],
            intermediate_size        = config["genomic_intermediate"],
            max_position_embeddings  = n_genes + 2,
            hidden_dropout_prob      = config["genomic_dropout"],
            attention_probs_dropout_prob = config["genomic_dropout"],
            vocab_size               = 1,
        )
        self.encoder   = BertModel(bert_cfg, add_pooling_layer=False)
        self.cls_token = nn.Parameter(torch.zeros(1, 1, H))

        # standalone head (used when training genomic branch alone)
        self.head = nn.Sequential(
            nn.Linear(H, 128), nn.ReLU(),
            nn.Dropout(config["genomic_dropout"]),
            nn.Linear(128, config["num_classes"])
        )

    def _embed(self, x):
        B, G = x.shape
        gene_emb = self.gene_proj(x.unsqueeze(-1))              # (B, G, H)
        cls      = self.cls_token.expand(B, -1, -1)             # (B, 1, H)
        seq      = torch.cat([cls, gene_emb], dim=1)            # (B, G+1, H)
        pos      = torch.arange(G + 1, device=x.device).unsqueeze(0)
        seq      = self.drop(self.norm(seq + self.pos_emb(pos)))
        return seq

    def forward(self, x):
        seq  = self._embed(x)
        mask = torch.ones(seq.shape[0], seq.shape[1], device=x.device)
        out  = self.encoder(inputs_embeds=seq, attention_mask=mask)
        return self.head(out.last_hidden_state[:, 0, :])

    def get_embedding(self, x):
        """Returns CLS embedding (B, hidden_size) for fusion."""
        seq  = self._embed(x)
        mask = torch.ones(seq.shape[0], seq.shape[1], device=x.device)
        out  = self.encoder(inputs_embeds=seq, attention_mask=mask)
        return out.last_hidden_state[:, 0, :]          # (B, H)

class SwinEncoder(nn.Module):
    """
    Wraps timm's Swin-Tiny.  Removes the classification head and projects
    the 768-d pooled feature to `swin_embed_dim` (default 256) so both
    modality embeddings share the same dimensionality going into fusion.
    """
    def __init__(self, config: dict, pretrained: bool = True):
        super().__init__()
        backbone = timm.create_model(
            config["swin_model"],
            pretrained  = pretrained,
            num_classes = 0,          # remove classification head → returns pooled features
        )
        # Swin-Tiny outputs 768-d after global average pooling
        swin_out_dim = backbone.num_features
        self.backbone = backbone
        self.proj = nn.Sequential(
            nn.Linear(swin_out_dim, config["swin_embed_dim"]),
            nn.LayerNorm(config["swin_embed_dim"]),
        )

    def forward(self, x):
        """x: (B, 3, H, W) → (B, swin_embed_dim)"""
        feats = self.backbone(x)      # (B, 768)
        return self.proj(feats)       # (B, 256)

class GatedFusion(nn.Module):
    """
    Learnable gating that weights each modality per-sample before summing.

    Given genomic embedding g ∈ R^D and image embedding v ∈ R^D:
        gate_g = σ(W_g · [g, v])
        gate_v = σ(W_v · [g, v])
        fused  = gate_g * g  +  gate_v * v

    This lets the model learn to rely more on whichever modality is
    informative for a given patient.
    """
    def __init__(self, dim: int):
        super().__init__()
        self.gate_g = nn.Linear(dim * 2, dim)
        self.gate_v = nn.Linear(dim * 2, dim)

    def forward(self, g, v):
        """g, v: (B, D) → fused: (B, D)"""
        cat    = torch.cat([g, v], dim=-1)          # (B, 2D)
        alpha  = torch.sigmoid(self.gate_g(cat))    # (B, D)
        beta   = torch.sigmoid(self.gate_v(cat))    # (B, D)
        return alpha * g + beta * v                 # (B, D)


class MultimodalFusionModel(nn.Module):
    """
    End-to-end fusion of GenomicBERT + SwinEncoder.

    Forward:
        gene_x  : (B, n_genes)   — scaled log1p gene expression
        image_x : (B, 3, 224, 224) — normalised histopathology patch
    Returns:
        logits  : (B, num_classes)
    """
    def __init__(self, n_genes: int, config: dict, pretrained_swin: bool = True):
        super().__init__()
        D = config["fusion_dim"]

        self.genomic_encoder = GenomicBERT(n_genes, config)
        self.image_encoder   = SwinEncoder(config, pretrained=pretrained_swin)
        self.fusion          = GatedFusion(D)

        self.classifier = nn.Sequential(
            nn.LayerNorm(D),
            nn.Linear(D, 128),
            nn.GELU(),
            nn.Dropout(config["fusion_dropout"]),
            nn.Linear(128, config["num_classes"])
        )

    def forward(self, gene_x, image_x):
        g_emb  = self.genomic_encoder.get_embedding(gene_x)    # (B, D)
        v_emb  = self.image_encoder(image_x)                    # (B, D)
        fused  = self.fusion(g_emb, v_emb)                      # (B, D)
        return self.classifier(fused)                           # (B, num_classes)

    def load_pretrained_branches(self, genomic_ckpt_path: str = None,
                                  swin_ckpt_path: str = None):
        """
        Optionally warm-start individual branches from their solo checkpoints.
        Skips loading if a path is None or the file doesn't exist.
        """
        if genomic_ckpt_path and os.path.exists(genomic_ckpt_path):
            ckpt = torch.load(genomic_ckpt_path, map_location="cpu", weights_only=False)
            state = ckpt.get("model_state", ckpt)
            # filter to keys that belong to genomic_encoder
            filtered = {k: v for k, v in state.items()
                        if not k.startswith("head.")}
            self.genomic_encoder.load_state_dict(filtered, strict=False)
            print(f"✓ Loaded GenomicBERT weights from {genomic_ckpt_path}")
        if swin_ckpt_path and os.path.exists(swin_ckpt_path):
            state = torch.load(swin_ckpt_path, map_location="cpu", weights_only=False)
            # Swin checkpoint saved as raw state_dict (no wrapper)
            self.image_encoder.backbone.load_state_dict(state, strict=False)
            print(f"✓ Loaded SwinTransformer weights from {swin_ckpt_path}")

class MultimodalDataset(Dataset):
    """
    Pairs a genomic vector with a randomly sampled image from the same class.

    In a real deployment you would pair samples by patient/slide ID.
    Here we fall back to class-level pairing when 1-to-1 IDs are not
    available — samples are drawn with replacement so every genomic sample
    gets an image partner at each epoch.

    Args:
        gene_X     : np.ndarray (N_g, n_genes)
        gene_y     : np.ndarray (N_g,)
        image_root : str — path to ImageFolder-style directory
        transform  : torchvision transform for images
    """
    def __init__(self, gene_X, gene_y, image_root, transform=None):
        self.gene_X    = torch.tensor(gene_X, dtype=torch.float32)
        self.gene_y    = torch.tensor(gene_y, dtype=torch.long)
        self.transform = transform

        # Build class → [image_paths] index
        self.class_images = {}
        for class_name in os.listdir(image_root):
            class_dir = os.path.join(image_root, class_name)
            if not os.path.isdir(class_dir):
                continue
            imgs = [os.path.join(class_dir, f) for f in os.listdir(class_dir)
                    if f.lower().endswith((".jpg", ".jpeg", ".png", ".bmp", ".tif", ".tiff"))]
            if imgs:
                self.class_images[class_name] = imgs

        # Map label index → class name (assumes ImageFolder ordering: alphabetical)
        self.idx_to_class = {i: c for i, c in enumerate(sorted(self.class_images.keys()))}
        print(f"Image classes: {self.idx_to_class}")
        print(f"Genomic samples: {len(self.gene_y)}")

    def __len__(self):
        return len(self.gene_y)

    def __getitem__(self, idx):
        gene_vec = self.gene_X[idx]
        label    = self.gene_y[idx].item()

        # sample a matching-class image
        class_name = self.idx_to_class.get(label)
        img_paths  = self.class_images.get(class_name, [])
        if img_paths:
            img_path = random.choice(img_paths)
            try:
                img = Image.open(img_path).convert("RGB")
            except Exception:
                img = Image.new("RGB", (224, 224))
        else:
            img = Image.new("RGB", (224, 224))

        if self.transform:
            img = self.transform(img)

        return gene_vec, img, torch.tensor(label, dtype=torch.long)


