@torch.no_grad()
def predict_single(model, gene_vector: np.ndarray, image_path: str,
                   scaler, transform, device, class_names=("Healthy", "Tumor")):
    """
    Predict the class of one patient given:
      - gene_vector : raw gene-expression array of shape (n_genes,) — BEFORE log1p / scaling
      - image_path  : path to a histopathology image
    """
    model.eval()

    # Preprocess genomic
    gene_log = np.log1p(gene_vector).reshape(1, -1).astype(np.float32)
    gene_sc  = scaler.transform(gene_log)
    gene_t   = torch.tensor(gene_sc, dtype=torch.float32).to(device)

    # Preprocess image
    img = Image.open(image_path).convert("RGB")
    img_t = transform(img).unsqueeze(0).to(device)           # (1, 3, H, W)

    logits = model(gene_t, img_t)
    probs  = F.softmax(logits, dim=1).squeeze().cpu().numpy()
    pred   = probs.argmax()

    print(f"Prediction : {class_names[pred]}  (confidence: {probs[pred]:.3f})")
    print(f"Class probs: { {n: f'{p:.3f}' for n, p in zip(class_names, probs)} }")
    return pred, probs


