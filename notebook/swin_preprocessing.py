def show_random_samples(healthy_files, diseased_files, n=8):
    samples = random.sample(healthy_files, n//2) + random.sample(diseased_files, n//2)
    labels = ["healthy"]*(n//2) + ["diseased"]*(n//2)

    plt.figure(figsize=(12, 6))

    for i, (f, label) in enumerate(zip(samples, labels)):
        dcm = pydicom.dcmread(f)
        img = dcm.pixel_array

        plt.subplot(2, n//2, i+1)
        plt.imshow(img, cmap='gray')
        plt.title(label)
        plt.axis('off')

    plt.suptitle("Random Samples (Healthy vs Diseased)", fontsize=14)
    plt.show()

