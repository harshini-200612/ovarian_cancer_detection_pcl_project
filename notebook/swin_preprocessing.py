def find_axial_series(patient_dir):
    all_dcm = glob.glob(
        os.path.join(patient_dir, '**', '*.dcm'),
        recursive=True
    )

    series_dict = {}

    for dcm_path in all_dcm:
        try:
            ds = pydicom.dcmread(dcm_path, stop_before_pixels=True)

            series_uid = str(getattr(ds, 'SeriesInstanceUID', 'unknown'))

            image_type = list(getattr(ds, 'ImageType', []))
            if 'LOCALIZER' in image_type or 'SCOUT' in image_type:
                continue

            desc = str(getattr(ds, 'SeriesDescription', '')).upper()

            # Skip non-axial
            if any(x in desc for x in ['CORONAL', 'SAGITTAL', 'MPR', 'REFORMAT']):
                continue

            if series_uid not in series_dict:
                series_dict[series_uid] = []

            series_dict[series_uid].append(dcm_path)

        except:
            continue

    if not series_dict:
        return all_dcm

    best_series = max(series_dict.values(), key=len)
    return sorted(best_series)

import numpy as np

def get_hu_image(dcm):
    img = dcm.pixel_array.astype(np.float32)

    slope = getattr(dcm, "RescaleSlope", 1)
    intercept = getattr(dcm, "RescaleIntercept", 0)

    return img * slope + intercept


def is_pelvic_slice(dcm):
    hu = get_hu_image(dcm)

    # Tissue + bone region
    mask = (hu > -200) & (hu < 300)

    ratio = np.sum(mask) / mask.size

    return ratio > 0.15

