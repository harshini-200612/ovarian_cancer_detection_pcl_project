def highlight_tumor_clean(dcm_path):
    # Load DICOM
    dcm = pydicom.dcmread(dcm_path)
    img = dcm.pixel_array.astype(np.float32)

    # Normalize
    img = (img - np.min(img)) / (np.max(img) - np.min(img))
    img = (img * 255).astype(np.uint8)

    h, w = img.shape

    # Focus center region (pelvic area)
    center_crop = img[int(h*0.2):int(h*0.8), int(w*0.2):int(w*0.8)]

    # Blur
    blurred = cv2.GaussianBlur(center_crop, (5,5), 0)

    # Adaptive threshold (better than fixed)
    thresh = cv2.adaptiveThreshold(
        blurred, 255,
        cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
        cv2.THRESH_BINARY,
        11, 2
    )

    # Find contours
    contours, _ = cv2.findContours(thresh, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    # Convert to color
    img_color = cv2.cvtColor(img, cv2.COLOR_GRAY2BGR)

    best_cnt = None
    max_area = 0

    for cnt in contours:
        area = cv2.contourArea(cnt)

        if 300 < area < 5000:  # filter noise + huge areas
            if area > max_area:
                max_area = area
                best_cnt = cnt

    # Draw best contour
    if best_cnt is not None:
        (x, y), radius = cv2.minEnclosingCircle(best_cnt)

        # Adjust back to original coordinates
        x += int(w*0.2)
        y += int(h*0.2)

        center = (int(x), int(y))
        radius = int(radius)

        cv2.circle(img_color, center, radius, (255, 0, 0), 3)
        cv2.putText(img_color, "Tumor Region",
                    (center[0]-40, center[1]-radius-10),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.5, (255,0,0), 1)

    # Plot
    plt.figure(figsize=(10,4))

    plt.subplot(1,2,1)
    plt.imshow(img, cmap='gray')
    plt.title("Original")

    plt.subplot(1,2,2)
    plt.imshow(img_color)
    plt.title("Tumor Highlighted")

    plt.axis('off')
    plt.show()

highlight_tumor_clean(dcm_path)