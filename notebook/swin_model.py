class DICOMDataset(Dataset):
    """
    PyTorch Dataset that reads .dcm files directly.
    No PNG/JPEG conversion. Works with raw DICOM.

    Each __getitem__ call:
      1. Reads the .dcm file with pydicom
      2. Converts to HU
      3. Applies soft-tissue windowing
      4. Resizes to 224x224
      5. Returns (tensor, label)
    """
    def __init__(self, paths, labels, config, augment=False):
        self.paths   = paths
        self.labels  = labels
        self.config  = config
        self.augment = augment

    def __len__(self):
        return len(self.paths)

    def __getitem__(self, idx):
        path  = self.paths[idx]
        label = self.labels[idx]

        # Read and preprocess the DICOM file
        tensor = read_dicom_slice(
            path,
            window_center=self.config['WINDOW_CENTER'],
            window_width =self.config['WINDOW_WIDTH'],
            img_size     =self.config['IMG_SIZE']
        )

        if tensor is None:
            # Return a blank tensor if DICOM read fails
            tensor = torch.zeros(3, self.config['IMG_SIZE'], self.config['IMG_SIZE'])

        # ── Optional augmentation (train only) ───────────────────
        if self.augment:
            tensor = self._augment(tensor)

        return tensor, torch.tensor(label, dtype=torch.long)

    def _augment(self, tensor):
        """Simple augmentations that work on tensors."""
        # Random horizontal flip
        if random.random() > 0.5:
            tensor = torch.flip(tensor, dims=[2])
        # Random vertical flip
        if random.random() > 0.5:
            tensor = torch.flip(tensor, dims=[1])
        # Random brightness jitter
        if random.random() > 0.5:
            factor = random.uniform(0.9, 1.1)
            tensor = tensor * factor
        return tensor


