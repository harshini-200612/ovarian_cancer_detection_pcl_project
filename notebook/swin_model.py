class SwinCTClassifier(nn.Module):
    """
    Swin-Tiny pretrained on ImageNet, fine-tuned for
    binary CT classification (healthy vs ovarian cancer).

    Input:  (B, 3, 224, 224)  — 3-channel grayscale CT slice
    Output: (B, 2)            — logits for [healthy, diseased]
    """
    def __init__(self, num_classes=2, dropout=0.3):
        super().__init__()
        # Load pretrained Swin-Tiny (same as your swin_model.ipynb)
        self.backbone = timm.create_model(
            'swin_tiny_patch4_window7_224',
            pretrained=True,
            num_classes=0   # remove head → returns 768-d features
        )
        feat_dim = self.backbone.num_features  # 768 for Swin-Tiny

        # Custom classification head
        self.head = nn.Sequential(
            nn.LayerNorm(feat_dim),
            nn.Dropout(dropout),
            nn.Linear(feat_dim, 256),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(256, num_classes)
        )

    def forward(self, x):
        features = self.backbone(x)   # (B, 768)
        return self.head(features)    # (B, 2)


