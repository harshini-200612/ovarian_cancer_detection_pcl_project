# ── define load_one_sample first ──────────────────────────────
def load_one_sample(sample_dir, expression_col):
    tsv_files = glob.glob(os.path.join(sample_dir, "*.tsv"))
    if not tsv_files:
        raise FileNotFoundError(f"No TSV found in {sample_dir}")
    df = pd.read_csv(tsv_files[0], sep="\t", comment="#", low_memory=False)
    df = df[~df["gene_id"].str.startswith("N_", na=False)]
    df = df[df["gene_id"] != "gene_id"]
    df = df.set_index("gene_id")
    expr = pd.to_numeric(df[expression_col], errors="coerce").fillna(0.0)
    return expr

# ── get top variable genes from 100 sample files ──────────────
def get_top_variable_genes(tumor_folders, healthy_folders, expression_col, top_n_genes):
    random.seed(42)
    sampled = (random.sample(tumor_folders, min(30, len(tumor_folders))) +
               random.sample(healthy_folders, min(30, len(healthy_folders))))
    series_list = []
    for folder in sampled:
        try:
            series_list.append(load_one_sample(folder, expression_col))
        except:
            pass
    df_sample = pd.DataFrame(series_list).fillna(0.0)
    top_genes = df_sample.var(axis=0).nlargest(top_n_genes).index.tolist()
    del df_sample, series_list
    gc.collect()
    print(f"Top {top_n_genes} variable genes selected from {len(sampled)} samples")
    return top_genes

# ── load all samples keeping only top genes ───────────────────
def load_all_samples_efficient(tumor_folders, healthy_folders, expression_col, top_genes):
    rows, labels = [], []
    print(f"Loading {len(tumor_folders)} tumor samples...")
    for folder in tumor_folders:
        try:
            s = load_one_sample(folder, expression_col)
            rows.append(s.reindex(top_genes, fill_value=0.0).values.astype(np.float32))
            labels.append(1)
        except Exception as e:
            print(f"  Skipping {os.path.basename(folder)}: {e}")
    print(f"Loading {len(healthy_folders)} healthy samples...")
    for folder in healthy_folders:
        try:
            s = load_one_sample(folder, expression_col)
            rows.append(s.reindex(top_genes, fill_value=0.0).values.astype(np.float32))
            labels.append(0)
        except Exception as e:
            print(f"  Skipping {os.path.basename(folder)}: {e}")
    X = np.array(rows, dtype=np.float32)
    y = np.array(labels, dtype=np.int64)
    print(f"\nLoaded: {sum(y==1)} tumor | {sum(y==0)} healthy")
    print(f"Feature matrix shape: {X.shape}")
    return X, y

