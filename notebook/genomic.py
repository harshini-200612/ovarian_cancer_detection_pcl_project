class GenomicBERT(nn.Module):
    """
    BERT-style transformer for RNA-seq gene expression.

    Each gene is treated as one token in the sequence.
    Self-attention captures co-expression dependencies across genes —
    the same idea as DNABERT capturing dependencies across k-mers.

    The CLS-token embedding (first position) is used for classification
    and also exported for multimodal fusion with your Swin image model.
    """

    def __init__(self, n_genes: int, config: dict):
        super().__init__()
        H = config["hidden_size"]

        # Project each gene's scalar value → hidden_size vector
        self.gene_proj = nn.Linear(1, H)

        # Learnable positional embedding (one per gene position + CLS)
        self.pos_emb = nn.Embedding(n_genes + 1, H)

        self.norm = nn.LayerNorm(H)
        self.drop = nn.Dropout(config["dropout"])

        # BERT encoder
        bert_cfg = BertConfig(
            hidden_size=H,
            num_attention_heads=config["num_attention_heads"],
            num_hidden_layers=config["num_hidden_layers"],
            intermediate_size=config["intermediate_size"],
            max_position_embeddings=n_genes + 2,
            hidden_dropout_prob=config["dropout"],
            attention_probs_dropout_prob=config["dropout"],
            vocab_size=1,   # not used — we supply embeddings directly
        )
        self.encoder = BertModel(bert_cfg, add_pooling_layer=False)

        # Learnable CLS token (shared across all samples)
        self.cls_token = nn.Parameter(torch.zeros(1, 1, H))

        # Classification head
        self.head = nn.Sequential(
            nn.Linear(H, 128),
            nn.ReLU(),
            nn.Dropout(config["dropout"]),
            nn.Linear(128, 2)
        )

    def _embed(self, x):
        B, G = x.shape

        # Gene embeddings: (B, G, H)
        gene_emb = self.gene_proj(x.unsqueeze(-1))

        # Prepend CLS token: (B, G+1, H)
        cls = self.cls_token.expand(B, -1, -1)
        seq = torch.cat([cls, gene_emb], dim=1)

        # Add positional embeddings
        pos = torch.arange(G + 1, device=x.device).unsqueeze(0)
        seq = self.drop(self.norm(seq + self.pos_emb(pos)))

        return seq

    def forward(self, x):
        seq = self._embed(x)
        mask = torch.ones(seq.shape[0], seq.shape[1], device=x.device)
        out = self.encoder(inputs_embeds=seq, attention_mask=mask)
        cls_repr = out.last_hidden_state[:, 0, :]   # CLS token → classify
        return self.head(cls_repr)

    def get_embeddings(self, x):
        """Returns CLS embedding for multimodal fusion. Shape: (B, hidden_size)"""
        seq = self._embed(x)
        mask = torch.ones(seq.shape[0], seq.shape[1], device=x.device)
        out = self.encoder(inputs_embeds=seq, attention_mask=mask)
        return out.last_hidden_state[:, 0, :]


