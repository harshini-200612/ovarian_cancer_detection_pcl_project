def train_one_epoch(model, loader, optimizer, criterion, device):
    model.train()
    total_loss, preds_all, labels_all = 0, [], []
    for X_b, y_b in loader:
        X_b, y_b = X_b.to(device), y_b.to(device)
        optimizer.zero_grad()
        logits = model(X_b)
        loss = criterion(logits, y_b)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        optimizer.step()
        total_loss += loss.item()
        preds_all.extend(logits.argmax(1).cpu().numpy())
        labels_all.extend(y_b.cpu().numpy())
    return total_loss / len(loader), accuracy_score(labels_all, preds_all)


@torch.no_grad()
def evaluate(model, loader, criterion, device):
    model.eval()
    total_loss, preds_all, labels_all, probs_all = 0, [], [], []
    for X_b, y_b in loader:
        X_b, y_b = X_b.to(device), y_b.to(device)
        logits = model(X_b)
        total_loss += criterion(logits, y_b).item()
        probs = torch.softmax(logits, dim=1)[:, 1].cpu().numpy()
        preds_all.extend(logits.argmax(1).cpu().numpy())
        labels_all.extend(y_b.cpu().numpy())
        probs_all.extend(probs)
    return (total_loss / len(loader),
            np.array(labels_all), np.array(preds_all), np.array(probs_all))

